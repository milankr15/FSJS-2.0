//1. Create a variable.js file and declare variables and assign string, boolean, undefined and null data types, Display all the value with their data type.

var strVariable = "Hello, World!"; // string
var boolVariable = true; // boolean
var undefinedVariable = undefined; // undefined
var nullVariable = null; // null

// Display the values and their data types
console.log("Value:", strVariable, "Data Type:", typeof strVariable);
console.log("Value:", boolVariable, "Data Type:", typeof boolVariable);
console.log("Value:", undefinedVariable, "Data Type:", typeof undefinedVariable);
console.log("Value:", nullVariable, "Data Type:", typeof nullVariable);
/// Output: Value: Hello, World! Data Type: string
        //  Value: true Data Type: boolean
        //  Value: undefined Data Type: undefined
        //  Value: null Data Type: object

